public class Variables {

    public static void main(String[] args) {
        byte DaByte = 3;
        System.out.println(DaByte);
        short DaShort = 32767;
        System.out.println(DaShort);
        int DaInt = 87607607;
        System.out.println(DaInt);
        long DaLong = 922330986;
        System.out.println(DaLong);
        float DaFloat = 333333333;
        System.out.println(DaFloat);
        double DaDouble = 3.2345465784547878979469769679;
        System.out.println(DaDouble);
        char DaChar = 'c';
        System.out.println(DaChar);
        boolean DaBool = false;
        System.out.println(DaBool);
    }
}
